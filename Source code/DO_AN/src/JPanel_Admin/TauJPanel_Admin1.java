    public void loadData(){
        tblTAUModel = (DefaultTableModel)jTable1.getModel();
        tblTAUModel.setRowCount(0);
        TauDao tauDao = new TauDao();
        arr = tauDao.docDanhMuc();
        
        String tieude[] = {"MATAU", "Tên tàu", "Hãng sản xuất", "Số ghế L1", "Số ghế L2", "Tổng số ghế", "Tình trạng"};
        tblTAUModel.setColumnIdentifiers(tieude);
        
        for(int i = 0; i < arr.size(); i++){
            
            Vector<Object> vec = new Vector<>();
            
            vec.add(arr.get(i).getMATAU());
            vec.add(arr.get(i).getTENTAU());
            vec.add(arr.get(i).getHANGSX()) ;
            vec.add(arr.get(i).getSOGHEL1()) ;
            vec.add(arr.get(i).getSOGHEL2()) ;
            vec.add(arr.get(i).getTONGSOGHE()) ;
            vec.add(arr.get(i).getTINHTRANG()) ;
            
            tblTAUModel.addRow(vec);
        }
    }  
    
    public void loadData_1(String maTAU){
        if(txtNhapMATAU.getText().equals("")){
            return;
        }
        else{
            maTAU = txtNhapMATAU.getText();
            
            tblTAUModel = (DefaultTableModel)jTable1.getModel();
            tblTAUModel.setRowCount(0);
            TauDao tauDao = new TauDao();
            arr = tauDao.Search(maTAU);

            String tieude[] = {"MATAU", "Tên tàu", "Hãng sản xuất", "Số ghế L1", "Số ghế L2", "Tổng số ghế", "Tình trạng"};
            tblTAUModel.setColumnIdentifiers(tieude);
            
            if(arr.size() > 0){
                for(int i = 0; i < arr.size(); i++){

                    Vector<Object> vec = new Vector<>();

                    vec.add(arr.get(i).getMATAU());
                    vec.add(arr.get(i).getTENTAU());
                    vec.add(arr.get(i).getHANGSX()) ;
                    vec.add(arr.get(i).getSOGHEL1()) ;
                    vec.add(arr.get(i).getSOGHEL2()) ;
                    vec.add(arr.get(i).getTONGSOGHE()) ;
                    vec.add(arr.get(i).getTINHTRANG()) ;
            
                    tblTAUModel.addRow(vec);
                }
                jlbTT.setText("Thành Công");
                jlbTT.setForeground(new Color(11,194,106));
                
            }
            else{
                jlbTT.setText("Không thành công");
                jlbTT.setForeground(Color.red);
                loadData();
            }
        }
    }

