    public void loadComboBox(){
        TaiKhoanDao tkDao = new TaiKhoanDao();
        arr = tkDao.loadComboBox();
        
        for(int i = 0; i < arr.size(); i++)
        {
            cbVaiTro.addItem(arr.get(i).getROLE());
        }
    }
    public void loadData(){
        tblTKModel = (DefaultTableModel)tblTK.getModel();
        tblTKModel.setRowCount(0);
        TaiKhoanDao tkDao = new TaiKhoanDao();
        arr = tkDao.docDanhMuc();
        
        String tieude[] = {"Tên đăng nhập", "Mật khẩu", "Vai trò"};
        tblTKModel.setColumnIdentifiers(tieude);
        
        for(int i = 0; i <arr.size();i++)
        {
            Vector<Object> vec = new Vector<>();
            vec.add(arr.get(i).getTENDN());
            vec.add(arr.get(i).getMATKHAU());
            vec.add(arr.get(i).getROLE());
            
            tblTKModel.addRow(vec);
        }
    }
    
    
    public void loadData_1(String TenDN){
        if(txtNhapTaiKhoan.getText().equals("")){
            return;
        }
        else{
            TenDN = txtNhapTaiKhoan.getText();
            
            tblTKModel = (DefaultTableModel) tblTK.getModel();
            tblTKModel.setRowCount(0);
            TaiKhoanDao tkDao = new TaiKhoanDao();
            arr = tkDao.Search(TenDN);
            
            String tieude[] = {"Tên đăng nhập", "Mật khẩu", "Vai trò"};
            tblTKModel.setColumnIdentifiers(tieude);
            
            if(arr.size()>0){
                for(int i = 0; i<arr.size(); i++){
                    Vector<Object> vec = new Vector<>();
                    
                    vec.add(arr.get(i).getTENDN());
                    vec.add(arr.get(i).getMATKHAU());
                    vec.add(arr.get(i).getROLE());
                    
                    tblTKModel.addRow(vec);
                }
                jlbTT.setText("Thành công");
                jlbTT.setForeground(new Color(11,194,106));
            }
            else{
                jlbTT.setText("Không thành công!");
                jlbTT.setForeground(Color.red);
                loadData();
                
            }
            
        }
    }
    
