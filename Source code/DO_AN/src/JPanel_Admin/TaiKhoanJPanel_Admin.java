    private void btnThemaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemaiKhoanActionPerformed
        TaiKhoanModel tk = new TaiKhoanModel();
        if(txtTenDN.getText().equals("")||txtMatKhau.getText().equals("")){
            jlbTT.setText("Bạn cần nhập đủ dữ liệu!");
            jlbTT.setForeground(Color.red);
            return;
        }
        else{
            tk.setTENDN(txtTenDN.getText());
            tk.setMATKHAU(txtMatKhau.getText());
            tk.setROLE(cbVaiTro.getSelectedItem().toString());
            tk.setTINHTRANG(1);
            
            TaiKhoanDao tkDao = new TaiKhoanDao();
            if(tkDao.AddTK(tk) > 0){
                jlbTT.setText("Thành công");
                jlbTT.setForeground(new Color(11,194,106));
                xoaTrang();
                tblTKModel.setRowCount(0);
                loadData();
            }
            else{
                jlbTT.setText("Không thành công!");
                jlbTT.setForeground(Color.red);
            }
        }
    }//GEN-LAST:event_btnThemaiKhoanActionPerformed

    private void btnXoaTaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaTaiKhoanActionPerformed
         int index = tblTK.getSelectedRow();
        if(index == -1){
            jlbTT.setText("Vui lòng chọn 1 Khách Hàng trong bảng để xóa!");
            jlbTT.setForeground(Color.red);
            return;
        }
        TaiKhoanDao tkDao = new TaiKhoanDao();
        int tk = JOptionPane.showConfirmDialog(this,"Bạn có muốn xóa không","Xác nhận" ,JOptionPane.YES_NO_OPTION);
        if(tk == JOptionPane.YES_OPTION){
            if(tkDao.DeleteTK(tblTK.getValueAt(index, 0).toString())){
                jlbTT.setText("Thành công");
                jlbTT.setForeground(new Color(11,194,106));
                xoaTrang();
                tblTKModel.setRowCount(0);
                loadData();
            }
            else{
                jlbTT.setText("Không thành công!");
                jlbTT.setForeground(Color.red);
            }      
        }
    }//GEN-LAST:event_btnXoaTaiKhoanActionPerformed

